// ----------------------------------------------------------------------------
//  Inclusions
// ----------------------------------------------------------------------------
#include <Arduino.h>

// ----------------------------------------------------------------------------
//  Parameters
// ----------------------------------------------------------------------------
#define BAUDRATE  9600

// ----------------------------------------------------------------------------
//  Declarations
// ----------------------------------------------------------------------------
uint16_t counter = 0;

// ----------------------------------------------------------------------------
//  Function prototypes
// ----------------------------------------------------------------------------
;


// ----------------------------------------------------------------------------
//  Setup
// ----------------------------------------------------------------------------
void setup() {
  //Initialize serial and wait for port to open:
  Serial.begin(BAUDRATE);
  while (!Serial) {
    ; 
  }

}

// ----------------------------------------------------------------------------
//  Loop
// ----------------------------------------------------------------------------
void loop() {

  // Print out current counter to Serial port as a single line
  Serial.println(counter);

  // Increment counter
  counter++;

  // Wait a second before dumping next line
  delay(1000);
}

// ----------------------------------------------------------------------------
//  Function definitions
// ----------------------------------------------------------------------------
;